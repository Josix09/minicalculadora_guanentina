#input
x=int(input("ingrese un número: "))

y=int(input("ingrese otro número: "))

opc=int(input("ingrese una opc: "))

#processing

if opc==1:
    print(x+y) #output

if opc==2:
    print(x-y) #output

if opc==3:
    print(x*y) #output

if opc==4:
    print(x/y) #output

if opc==5:    
    print(x^y) #output


    